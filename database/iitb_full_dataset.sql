-- SCHEMA
CREATE TABLE classrooms (
  id SERIAL PRIMARY KEY,
  block VARCHAR(10),
  room_name VARCHAR(20),
  capacity INT,
  current_occupancy INT DEFAULT 0,
  floor INT,
  gps_h3 VARCHAR(20),
  distance_from_central INT,
  status VARCHAR(20) DEFAULT 'open'
);

CREATE TABLE parking_spots (
  id SERIAL PRIMARY KEY,
  zone VARCHAR(30),
  spot_number VARCHAR(10),
  status VARCHAR(10) DEFAULT 'empty',
  type VARCHAR(10) DEFAULT 'car',
  gps_h3 VARCHAR(20),
  distance_from_central INT,
  fullness_pct INT DEFAULT 0
);

CREATE TABLE distances (
  from_block VARCHAR(10),
  to_block VARCHAR(10),
  to_parking_zone VARCHAR(30),
  distance_m INT,
  walking_min DECIMAL(3,1)
);

